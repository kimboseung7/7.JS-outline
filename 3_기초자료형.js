const myName = "둘리"; //큰 따움표
const email = 'user@naver.com'; //작은 따움표
//백틱. ${} 기호를 사용해서 문자열에 변수를 포함시킴
const hello = `hello ${myName}`; 

console.log(myName);
console.log(email)
console.log(hello)

//Number (숫자)
const number = 123; //정수
const opacity = 0.57; //실수
console.log(number);
console.log(opacity);

//Boolean (~여부, 논리형)
let checked = true;
let isShow = false;
console.log(checked);
console.log(isShow);

let udef; // 값을 대입하지 않으면 undefined
